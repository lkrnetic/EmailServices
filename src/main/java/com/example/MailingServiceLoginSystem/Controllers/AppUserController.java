    @PostMapping("/signup")
    public RedirectView createNewAppUser(@RequestParam String firstName , @RequestParam String lastName,
                                         @RequestParam String email , @RequestParam String password){
        boolean isValidEmail = emailValidator.test(email);
        if (!isValidEmail) {
            throw new IllegalStateException("email not valid");
        }
        AppUser appUser =
                new AppUser(
                        firstName,
                        lastName,
                        email,
                        password,
                        AppUserRole.USER
                );
        /*
        boolean userExists = appUserRepository.findByEmail(appUser.getEmail()).isPresent();
        if (userExists) {
            throw new IllegalStateException("email already taken");
        }
        */
        String token = UUID.randomUUID().toString();
        ConfirmationToken confirmationToken = new ConfirmationToken(
                token,
                LocalDateTime.now(),
                LocalDateTime.now().plusMinutes(15),
                appUser
        );
        String encodedPassword = bCryptPasswordEncoder.encode(appUser.getPassword());
        appUser.setPassword(encodedPassword);
        appUserRepository.save(appUser);
        confirmationTokenService.saveConfirmationToken(confirmationToken);
        String link = "http://localhost:8080/confirm?token=" + token;
        emailSender.send(
                email,
                buildEmail(firstName, link));
        //set the login token session
        Authentication authentication = new UsernamePasswordAuthenticationToken(appUser, null , appUser.getAuthorities());
        SecurityContextHolder.getContext().setAuthentication(authentication);
        return new RedirectView("/login");
    }

    @GetMapping("/login")
    public String getLoginPage(){
        return "login" ;
    }


    @GetMapping("/logout")
    public String getLogoutPage(){
        return "logout" ;
    }



    @GetMapping("/login-error")
    public String login(HttpServletRequest request, Model model) {
        HttpSession session = request.getSession(false);
        String errorMessage = null;
        if (session != null) {
            AuthenticationException ex = (AuthenticationException) session
                    .getAttribute(WebAttributes.AUTHENTICATION_EXCEPTION);
            if (ex != null) {
                errorMessage = ex.getMessage();
            }
        }
        model.addAttribute("errorMessage", errorMessage);
        return "login";
    }


    @GetMapping("/confirm")
    public String confirm(@RequestParam("token") String token) {
         appUserService.confirmToken(token);
         return "emails";
    }

